package HomeworkDos;

import java.util.HashSet;
import java.util.Set;

public class day19_HashSetTask2 {
    public static void main(String[] args) {
        Set<String> color = new HashSet<>();
        color.add("Red");
        color.add("Blue");
        color.add("Black");
        color.add("Pink");
        color.add("Yellow");
        for (String a : color) {
            System.out.println(a);
        }
        }
        }

